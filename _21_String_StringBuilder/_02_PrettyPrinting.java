package DsaProgress._21_String_StringBuilder;

public class _02_PrettyPrinting {
    public static void main(String[] args) {
        float a = 43.13476f;
        System.out.printf("Formatted number : %.2f", a);
        System.out.println();
        System.out.println(Math.PI);
        System.out.printf("Formatted pi : %.3f", Math.PI);
/*
There are many format specifiers we can use. Here are some common ones:

%c - Character
%d - Decimal number (base 10)
%e - Exponential floating-point number
%f - Floating-point number
%i - Integer (base 10)
%o - Octal number (base 8)
%s - String
%u - Unsigned decimal (integer) number
%x - Hexadecimal number (base 16)
%t - Date/time
%n - Newline

 */
    }
}
